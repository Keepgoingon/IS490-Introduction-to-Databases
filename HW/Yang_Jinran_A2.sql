CREATE TABLE JINRANY2_A2_STUDENT
(SID     NUMBER(9)     NOT NULL,
 LastNM  VARCHAR2(20)  NOT NULL,
 FirstNM VARCHAR2(20)  NOT NULL,
 Street  VARCHAR2(20)  NOT NULL,
 City    VARCHAR2(20)  NOT NULL,
 State   VARCHAR2(20)  NOT NULL,
 ZIP     VARCHAR2(20)  NOT NULL,
 Credit INTEGER       NOT NULL ,
 PRIMARY KEY (SID));
grant select on JINRANY2_A2_STUDENT to GSLISDM
grant select on JINRANY2_A2_STUDENT to jguo24

 CREATE TABLE JINRANY2_A2_COURSE
 (CourseNO   VARCHAR2(20) NOT NULL,
  CourseNM   VARCHAR2(40) NOT NULL,
  Location   VARCHAR2(20) NOT NULL,
  Instructor VARCHAR2(20) DEFAULT 'Staff' NOT NULL,
  PRIMARY KEY (CourseNO));
grant select on JINRANY2_A2_COURSE to GSLISDM
grant select on JINRANY2_A2_COURSE to jguo24

 CREATE TABLE JINRANY2_A2_REGISTRATION
 (SID     NUMBER(9)    NOT NULL,
 CourseNO VARCHAR2(20) NOT NULL,
 PRIMARY KEY (SID,CourseNO),
 FOREIGN KEY (SID) REFERENCES JINRANY2_A2_STUDENT(SID)
 ON DELETE CASCADE,
 FOREIGN KEY (CourseNO) REFERENCES JINRANY2_A2_COURSE(CourseNO)
 ON DELETE CASCADE);
grant select on JINRANY2_A2_REGISTRATION to GSLISDM
grant select on JINRANY2_A2_REGISTRATION to jguo24

